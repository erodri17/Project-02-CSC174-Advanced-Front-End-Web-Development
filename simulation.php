							 <p>From three basic types of strategic, planning, and learning exercises: games, simulations, and case studies, a number of hybrids may be considered, including simulation games that are used as case studies.</p>	
							 
						 <h3 class="c-heading c-heading--large">History</h3>
						
							 <p>While many credit simulation games beginning with Will Wright and SimCity in 1989, the true progenitor of the genre was "Fortune Builder", released in 1984 on Colecovision. Certain games such as SimLife and SimEarth were subsequently created and are capable of teaching players the basics of genetics and global ecosystems.</p>	

							 <p>In a study where adolescents played SimCity 2000, the study found that those participants who played the game had a greater appreciation and expectation of their government officials after playing.</p>	
							 
						 <h3 class="c-heading c-heading--large">Construction and management simulation</h3>		 
							 
							 <p>Construction and management simulation (CMS) is a type of simulation game in which players build, expand or manage fictional communities or projects with limited resources. Strategy games sometimes incorporate CMS aspects into their game economy, as players must manage resources while expanding their project. But pure CMS games differ from strategy games in that "the player's goal is not to defeat an enemy, but to build something within the context of an ongoing process." Games in this category are sometimes also called "management games".</p>	
							 
							 
						 <h3 class="c-heading c-heading--large">Life simulation</h3>		 
							 
							 <p>Life simulation games (or artificial life games) is a subgenre of simulation video games in which the player lives or controls one or more artificial lifeforms. A life simulation game can revolve around "individuals and relationships, or it could be a simulation of an ecosystem".</p>	
							 
							 
						 <h3 class="c-heading c-heading--large">Sports</h3>		 
							 
							 <p>A sports game is a video game that simulates the playing of sports. Most sports have been recreated with a game, including team sports, athletics and extreme sports. Some games emphasize actually playing the sport (such as the Madden NFL series), whilst others emphasize strategy and organization (such as Championship Manager). Some, such as Arch Rivals, satirize the sport for comic effect. This genre has been popular throughout the history of video games and is competitive, just like real-world sports. A number of game series feature the names and characteristics of real teams and players, and are updated annually to reflect real-world changes.</p>	
							 							 
	
	
