						 <h2 class="c-heading c-heading--xlarge">Game design</h2>

							<h3 class="c-heading c-heading--large">Levels</h3>

								<p>Players advance through an action game by completing a series of levels. Levels are often grouped by theme, with similar graphics and enemies. Each level involves a variety of challenges, whether dancing in a dance game or shooting things in a shooter, which the player must overcome to win the game. Older games force players to restart a level after dying, although action games evolved to offer saved games and checkpoints to allow the player to restart partway through a level. Increasingly, though, some games allow for 'resurrection' or 'cloning' and the opportunity to regain lost items upon death for a certain sum of ingame currency, typically increasing exponentially the more times the player dies. The obstacles and enemies in a level do not usually vary between play sessions, allowing players to learn by trial and error. However, levels sometimes add an element of randomness, such as an enemy that randomly appears or that takes an unpredictable path.</p>

								<p>Levels in an action game may be linear or nonlinear, and sometimes include shortcuts. For levels that require exploration, the player may need to search for a level exit that is hidden or guarded by enemies. Such levels can also contain secrets—hidden or hard-to-reach objects or places that contain something valuable. The prize can be a bonus (see below) or a non-standard exit that allows a player to access a hidden level, or jump ahead several levels. Action games sometimes offer a teleporter that will cause the player's avatar to re-appear somewhere else in the same level. Levels often make use of locked doors that can only be opened with a specific key found somewhere else in the level.</p>

								<p>Action games sometimes make use of time restrictions to increase the challenge. However, game levels typically do not react to time passing, and day/night cycles are rare. When the timer expires, the player typically loses a life, although some games generate a difficult enemy or challenge. If the level is completed with time remaining, this usually adds to the player's score.</p>

							<h3 class="c-heading c-heading--large">Character abilities</h3>

								<p>In most action games, the player controls a single avatar as the protagonist. The avatar has the ability to navigate and maneuver, and often collects or manipulates objects. They have a range of defenses and attacks, such as shooting or punching. Many action games make use of a powerful attack that destroys all enemies within a limited range, but this attack is rare.</p>

								<p>Players may find a power-up within the game world that grants temporary or permanent improvements to their abilities. For example, the avatar may gain an increase in speed, more powerful attacks, or a temporary shield from attacks. Some action games even allow players to spend upgrade points on the power ups of their choice. In action games, most of the avatar's character development comes from power-ups and new moves, and mental states do not usually change or progress.</p>

							<h3 class="c-heading c-heading--large">Obstacles and enemies</h3>

								<p>In action games that involve navigating a space, players will encounter obstacles, traps, and enemies. Enemies typically follow fixed patterns and attack the player, although newer action games may make use of more complex artificial intelligence to pursue the player. Enemies sometimes appears in groups or waves, with enemies increasing in strength and number until the end of the level. Enemies may also appear out of thin air. This can involve an invisible spawn point, or a visible generator which can be destroyed by the player. These points may generate enemies indefinitely, or only up to a certain number. At the end of a level or group of themed levels, players often encounter a boss. This boss enemy will often resemble a larger or more difficult version of a regular enemy. A boss may require a special weapon or attack method, such as striking when the boss opens their mouth or attacking particular part of the Boss.</p>

							<h3 class="c-heading c-heading--large">Health and lives</h3>

								<p>In many action games, the avatar has a certain amount of hitmarkers or health, which are depleted by enemy attacks and other hazards. Sometimes health can be replenished by collecting an in-game object. When the player runs out of health, the player dies. The player's avatar is often given a small number of chances to retry after death, typically referred to as lives. Upon beginning a new life, the player resumes the game either from the same location they died, a checkpoint, or the start of the level. Upon starting a new life, the avatar is typically invincible for a few seconds to allow the player to re-orient themselves. Players may earn extra lives by reaching a certain score or by finding an in-game object. Arcade games still limit the number of player lives, while home video games have shifted increasingly to unlimited lives.</p>


							
